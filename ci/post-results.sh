#!/usr/bin/env bash
# =============================================================================
# Weka Manila CI - Post Results to Gerrit
#
# Usage: post-results.sh <change> <patchset> <revision> <SUCCESS|FAILURE> <message> <log_dir>
# =============================================================================

set -uo pipefail

CHANGE_NUM="$1"
PATCHSET_NUM="$2"
REVISION="$3"
RESULT="$4"
MESSAGE="$5"
LOG_DIR="$6"

# ── Configuration ─────────────────────────────────────────────────────────────

CI_NAME="Weka Manila CI"
GERRIT_HOST="review.opendev.org"
GERRIT_PORT=29418
GERRIT_USER="${GERRIT_USER:-Assaf}"
LOG_BASE_URL="${CI_LOG_URL:-http://$(hostname -I | awk '{print $1}'):8088}"
LOG_URL="${LOG_BASE_URL}/${CHANGE_NUM}/${PATCHSET_NUM}/"

# Set to "true" once the Manila team grants voting rights
VOTING_ENABLED="${VOTING_ENABLED:-false}"

# ── Build comment ─────────────────────────────────────────────────────────────

if [ "$RESULT" = "SUCCESS" ]; then
    STATUS_LINE="Build succeeded (${CI_NAME})"
    VOTE="+1"
else
    STATUS_LINE="Build failed (${CI_NAME})"
    VOTE="-1"
fi

COMMENT="${STATUS_LINE}

${MESSAGE}

- Logs: ${LOG_URL}
- Console: ${LOG_URL}ci-runner.log"

# Add tempest results link if available
if [ -f "${LOG_DIR}/testr_results.html" ]; then
    COMMENT="${COMMENT}
- Test results: ${LOG_URL}testr_results.html"
fi

# ── Post to Gerrit ────────────────────────────────────────────────────────────

REVIEW_ARGS=()
if [ "$VOTING_ENABLED" = "true" ]; then
    REVIEW_ARGS+=(--verified "$VOTE")
fi

echo "Posting ${RESULT} to Gerrit for ${CHANGE_NUM},${PATCHSET_NUM}"

ssh -p "$GERRIT_PORT" "${GERRIT_USER}@${GERRIT_HOST}" \
    gerrit review \
    "${REVIEW_ARGS[@]}" \
    --tag "autogenerated:weka-manila-ci" \
    -m "'${COMMENT}'" \
    "${CHANGE_NUM},${PATCHSET_NUM}"

echo "Posted successfully"
